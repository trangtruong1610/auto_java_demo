package lesson2;

public class SimpleOperators {
    public static void main(String[] args) {
        int myAge = 29;
        myAge = myAge + 1;
        System.out.println(myAge);
        myAge++;
        System.out.println(myAge);
        myAge += 1;
        System.out.println(myAge);
        boolean isThatTrue = myAge <= 30;
        System.out.println(isThatTrue);

    }
}
