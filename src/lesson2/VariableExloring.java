package lesson2;

public class VariableExloring {
    public static void main(String[] args) {
        String myName = "Trang Truong";
        int myAge = 29;
        System.out.println("My name is " + myName + " and I'm " + myAge + " years old");
        System.out.printf("My name is %s and I'm %d years old", myName, myAge);

    }
}
